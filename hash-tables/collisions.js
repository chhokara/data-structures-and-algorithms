// Separate Chaining
// With separate chaining, at each index we use a more sophisticated data structure
// (i.e. array, linked list) to store our values
// This allows us to store multiple key value pairs at the same index

// Linear Probing
// With linear probing, when we find a collision, we search through the array to
// find the next empty slot.
// Unlike with separate chaining, this allows us to store a single key-value at 
// each index.