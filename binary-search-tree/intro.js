// What is a tree
// A data structure that consists of nodes in a parent-child 
// relationship

// Root - top node in a tree
// Child - A node directly connected to another node moving away from root
// Parent - the converse of a child
// Siblings - A group of nodes with the same parent
// Leaf - A node with no children
// Edge - The connection between one node and another

// Binary Search Trees
// Special case of binary trees where each node can have at most two children
// Every node to left of parent is less than parent
// Every node to right of parent is greater than parent